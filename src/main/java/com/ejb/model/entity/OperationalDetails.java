package com.ejb.model.entity;

import java.io.Serializable;
import java.util.Collection;
import javax.persistence.Basic;
import javax.persistence.CascadeType;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Lob;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlTransient;

/**
 *
 * @author Sameera
 */
@Entity
@Table(name = "operational_details")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "OperationalDetails.findAll", query = "SELECT o FROM OperationalDetails o")
    , @NamedQuery(name = "OperationalDetails.findById", query = "SELECT o FROM OperationalDetails o WHERE o.id = :id")
    , @NamedQuery(name = "OperationalDetails.findByName", query = "SELECT o FROM OperationalDetails o WHERE o.name = :name")})
public class OperationalDetails implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Size(max = 1450)
    @Column(name = "name")
    private String name;
    @Lob
    @Size(max = 65535)
    @Column(name = "description")
    private String description;
    @OneToMany(cascade = CascadeType.ALL, mappedBy = "operationalDetailsId")
    private Collection<EmployeeOperationalDetails> employeeOperationalDetailsCollection;

    public OperationalDetails() {
    }

    public OperationalDetails(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    @XmlTransient
    public Collection<EmployeeOperationalDetails> getEmployeeOperationalDetailsCollection() {
        return employeeOperationalDetailsCollection;
    }

    public void setEmployeeOperationalDetailsCollection(Collection<EmployeeOperationalDetails> employeeOperationalDetailsCollection) {
        this.employeeOperationalDetailsCollection = employeeOperationalDetailsCollection;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof OperationalDetails)) {
            return false;
        }
        OperationalDetails other = (OperationalDetails) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.ejb.model.entity.OperationalDetails[ id=" + id + " ]";
    }
    
}
